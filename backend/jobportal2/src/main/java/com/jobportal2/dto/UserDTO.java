package com.jobportal2.dto;

import com.jobportal2.entity.Role;

public class UserDTO {

    private Long id;
    private String name;
    private String email;
    private Role role;

    // No-arg constructor (required by Jackson / Spring)
    public UserDTO() {
    }

    // Convenience constructor
    public UserDTO(Long id, String name, String email, Role role) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.role = role;
    }

    // ===== Getters & Setters =====

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public Role getRole() {
        return role;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setRole(Role role) {
        this.role = role;
    }
}
